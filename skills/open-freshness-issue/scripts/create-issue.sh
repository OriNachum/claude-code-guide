#!/usr/bin/env bash
set -euo pipefail

# create-issue.sh — Create a GitHub issue from docs-freshness findings
#
# Usage:
#   create-issue.sh [findings.json]
#   echo '<json>' | create-issue.sh
#
# Input: JSON array of findings, each with:
#   { "file": "tier/name.md", "finding": "description", "source_url": "https://..." }
#
# Environment:
#   FRESHNESS_PR_URL — optional link to an associated PR (included in issue body)
#   GH_TOKEN / GITHUB_TOKEN — GitHub auth (automatic in CI)
#
# Output: Issue URL on stdout, or a skip message.

# --- dependency check ---
for cmd in gh jq; do
  if ! command -v "$cmd" >/dev/null 2>&1; then
    echo "ERROR: $cmd is required but not installed." >&2
    exit 1
  fi
done

# --- read findings ---
if [ -n "${1:-}" ] && [ -f "$1" ]; then
  FINDINGS="$(cat "$1")"
else
  FINDINGS="$(cat)"
fi

# --- validate ---
if ! echo "$FINDINGS" | jq empty 2>/dev/null; then
  echo "ERROR: Invalid JSON input." >&2
  exit 1
fi

COUNT="$(echo "$FINDINGS" | jq 'length')"
if [ "$COUNT" -eq 0 ]; then
  echo "No findings — skipping issue creation."
  exit 0
fi

# --- dedup: check for existing open issue ---
EXISTING="$(gh issue list --label "docs-freshness" --state open --json number --jq '.[0].number // empty' 2>/dev/null || true)"
if [ -n "$EXISTING" ]; then
  REPO="$(gh repo view --json nameWithOwner -q .nameWithOwner 2>/dev/null || echo "unknown")"
  echo "Open docs-freshness issue #${EXISTING} already exists — skipping."
  echo "https://github.com/${REPO}/issues/${EXISTING}"
  exit 0
fi

# --- build table rows ---
TABLE="$(echo "$FINDINGS" | jq -r '.[] | "| \(.file) | \(.finding) | [\((.source_url // "") | split("/") | last)](\(.source_url // "")) |"')"

# --- build issue body ---
DATE="$(date +%Y-%m-%d)"

BODY="## Docs Freshness Report --- ${DATE}

The following reference docs may contain outdated or inaccurate information
compared to the [official Anthropic documentation](https://docs.anthropic.com/en/docs/claude-code/overview).

| File | Finding | Source |
|------|---------|--------|
${TABLE}

### Summary

- **Files with issues:** ${COUNT}
- **Date checked:** ${DATE}"

if [ -n "${FRESHNESS_PR_URL:-}" ]; then
  BODY="${BODY}
- **Associated PR:** ${FRESHNESS_PR_URL}"
fi

BODY="${BODY}

---

*Generated by the docs-freshness workflow.*"

# --- create issue via temp file for safe escaping ---
TMPFILE="$(mktemp)"
trap 'rm -f "$TMPFILE"' EXIT
echo "$BODY" > "$TMPFILE"

ISSUE_URL="$(gh issue create \
  --title "docs: freshness findings (${DATE})" \
  --label "docs-freshness" \
  --body-file "$TMPFILE")"

echo "$ISSUE_URL"
